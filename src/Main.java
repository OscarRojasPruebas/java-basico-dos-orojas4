//Ejercicio con FOR
public class Main {
    public static void main(String[] args) {
         int suma = 0;

         for (int i = 1; i <= 10; i++) {
             suma += i;
         }
         System.out.println("La sumatoria de los números comprendidos entre 1 y 10 es: " + suma);
  }
}

